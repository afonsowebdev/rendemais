  function detect() {
    try {
      const saved = localStorage.getItem("rende_lang");
      if (saved && SUP.indexOf(saved) >= 0) return saved;
    } catch (e) {}
    const n = (navigator.language || "pt").slice(0, 2).toLowerCase();
    return SUP.indexOf(n) >= 0 ? n : "pt";
  }
  function save(l) { try { localStorage.setItem("rende_lang", l); } catch (e) {} }
  function make(lang) {
    const table = dict[lang] || dict.pt;
    return function (key) {
      if (table[key] != null) return table[key];
      if (dict.pt[key] != null) return dict.pt[key];
      return key;
    };
  }

  // ---- store reativo (fonte única do idioma para toda a app) ----
  let _lang = detect();
  const _subs = new Set();
  function getLang() { return _lang; }
  function setLangGlobal(l) { if (SUP.indexOf(l) < 0) return; _lang = l; save(l); _subs.forEach(function (fn) { fn(l); }); }
  function subscribe(fn) { _subs.add(fn); return function () { _subs.delete(fn); }; }

  return { dict, SUP, LABELS, detect, save, make, getLang, setLangGlobal, subscribe };
